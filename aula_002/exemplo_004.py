# Booleanos (bool)

# Operações lógicas:
# and (E lógico)
print(f"Booleano TRUE com o operador AND '5==5 and 5==5': {5==5 and 5==5}")
print(f"Booleano FALSE com o operador AND '5==5 and 5==5': {5==4 and 5==5}")

# or (OU lógico)
print(f"Booleano TRUE com o operador OR '5==5 or 5==5': {5==5 or 5==4}")
print(f"Booleano FALSE com o operador OR '5==5 or 5==5': {5==9 or 5==4}")

