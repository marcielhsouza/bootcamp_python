#Números de Ponto Flutuante (float)

#Métodos e operações:
# + (adição)
print(f"Adição: {4.9} + {4.9} = {4.9+4.9}")   

# - (subtração)
print(f"Subtração: {4.9} - {2.9} = {4.9-2.9}")  

# * (multiplicação)
print(f"Multiplicação: {4.9} * {2.9} = {4.9*2.9}")  

# / (divisão)
print(f"Divisão: {4.9} / {2.9} = {4.9/2.9}") 

