#Inteiros (int)

#Métodos e operações:
#        + (adição)
print(f"Adição: {10} + {10} = {10+10}")    

#        - (subtração)
print(f"Subtração: {20} - {10} = {20-10}")   

#        * (multiplicação)
print(f"Multiplicação: {5} * {5} = {5*5}")

#        // (divisão inteira)
print(f"Divisão Inteira: {5} // {5} = {5//5}")
#        % (módulo - resto da divisão)
